#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn catalog_rectangular_and_nonempty() {
        for p in CATALOG {
            let grid = p.cells();
            assert!(p.rows() > 0 && p.cols() > 0, "{} empty", p.id);
            assert!(grid.iter().all(|r| r.len() == p.cols()), "{} ragged", p.id);
            assert!(grid.iter().flatten().any(|&c| c), "{} all-blank", p.id);
        }
    }

    #[test]
    fn ids_unique() {
        let mut ids: Vec<&str> = CATALOG.iter().map(|p| p.id).collect();
        let n = ids.len();
        ids.sort_unstable();
        ids.dedup();
        assert_eq!(ids.len(), n, "duplicate picture id");
    }

    /// Reports which pictures are fair (line-solvable). Non-fair ones are
    /// auto-hidden at runtime; this just surfaces the count so we know the
    /// real pack size. Run with `--nocapture` to see the list.
    #[test]
    fn report_fair_pictures() {
        let mut good = 0;
        for p in CATALOG {
            if p.is_good() {
                good += 1;
            } else {
                eprintln!("AMBIGUOUS (hidden): {} ({})", p.id, p.title);
            }
        }
        eprintln!("fair pictures: {}/{}", good, CATALOG.len());
        assert!(good > 0, "no fair pictures at all");
    }
}
