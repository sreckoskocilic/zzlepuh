//! Designed "picture" nonograms — original public-domain pixel-art silhouettes.
//! Solving one reveals the image. Art: '#' = filled, anything else = blank.
//!
//! A picture is only offered if it's *line-solvable* (pure constraint
//! propagation solves it with no guessing) — that guarantees a unique solution
//! AND a fair, logic-only puzzle. Ambiguous art is filtered out automatically.

use super::solver;
use super::types::{clues_from_grid, NonogramPuzzle};

pub struct Picture {
    pub id: &'static str,
    pub title: &'static str,
    art: &'static [&'static str],
}

impl Picture {
    pub fn cells(&self) -> Vec<Vec<bool>> {
        let cols = self.cols();
        self.art
            .iter()
            .map(|line| {
                let mut row: Vec<bool> = line.chars().map(|c| c == '#').collect();
                row.resize(cols, false); // pad ragged rows
                row
            })
            .collect()
    }

    pub fn rows(&self) -> usize {
        self.art.len()
    }

    pub fn cols(&self) -> usize {
        self.art.iter().map(|l| l.chars().count()).max().unwrap_or(0)
    }

    /// Fair + unique check: solvable by line propagation alone.
    pub fn is_good(&self) -> bool {
        let (row_clues, col_clues) = clues_from_grid(&self.cells());
        solver::is_line_solvable(&row_clues, &col_clues, self.rows(), self.cols())
    }

    pub fn to_puzzle(&self) -> NonogramPuzzle {
        let (row_clues, col_clues) = clues_from_grid(&self.cells());
        NonogramPuzzle {
            rows: self.rows(),
            cols: self.cols(),
            row_clues,
            col_clues,
            difficulty: "picture".to_string(),
            title: Some(self.title.to_string()),
        }
    }
}

pub fn by_id(id: &str) -> Option<&'static Picture> {
    CATALOG.iter().find(|p| p.id == id)
}

